/* Phase 3 — split lib/translations.ts into lib/i18n/en.ts + lib/i18n/hi.ts.
 *
 * The original file is one big `export const translations = { en: {...}, hi: {...} }`.
 * We slice out the inner object literals for each language and write them as
 * their own modules. The entry module (lib/translations.ts) then re-exports EN
 * eagerly (keeps first-paint text correct) while hi.ts can be dynamic-imported
 * by the language context only when the user actually switches. */

const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const SRC = path.join(ROOT, "lib", "translations.ts");
const I18N_DIR = path.join(ROOT, "lib", "i18n");

const raw = fs.readFileSync(SRC, "utf8").split(/\r?\n/);

// Line numbers from the grep (1-indexed):
//   2    en: {
//   1561   },           (closes en object)
//   1563 hi: {
//   3109   },           (closes hi object)
//
// We want the BODY of each object literal (the key/value lines), so:
//   - EN body: lines 3..1560 (0-indexed: 2..1559)
//   - HI body: lines 1564..3108 (0-indexed: 1563..3107)

const enBody = raw.slice(2, 1560).join("\n");
const hiBody = raw.slice(1563, 3108).join("\n");

const enContent = `// English translations. Eagerly bundled by the language context so the
// first paint of any route shows correctly even before the optional Hindi
// bundle is loaded.
//
// Generated by scripts/split-translations.cjs — edit in place, do not
// regenerate unless the canonical list changes.

// Note: no \`as const\` here — we want \`EnTranslations\` to describe the
// KEY SHAPE (specific keys, string values) so Hindi can satisfy the same
// type with different string values.

export const en = {
${enBody}
};

export type EnTranslations = typeof en;
`;

const hiContent = `// Hindi translations. Lazy-loaded: the language context imports this file
// only once the user switches to Hindi, so English-only visitors never pay
// for it.
//
// Shape must match EnTranslations exactly; TypeScript enforces this via the
// \`EnTranslations\` annotation below.

import type { EnTranslations } from "./en";

export const hi: EnTranslations = {
${hiBody}
};
`;

fs.mkdirSync(I18N_DIR, { recursive: true });
fs.writeFileSync(path.join(I18N_DIR, "en.ts"), enContent);
fs.writeFileSync(path.join(I18N_DIR, "hi.ts"), hiContent);
console.log(
  `wrote en.ts (${enContent.length} bytes) + hi.ts (${hiContent.length} bytes)`
);
